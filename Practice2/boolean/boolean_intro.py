is_raining = True
is_sunny = False
print("Is it raining?", is_raining)
print("Is it sunny?", is_sunny)
print("Is it both raining and sunny?", is_raining and is_sunny)
print("Is it either raining or sunny?", is_raining or is_sunny)
print("Is it not raining?", not is_raining)
print("Is it not sunny?", not is_sunny)
